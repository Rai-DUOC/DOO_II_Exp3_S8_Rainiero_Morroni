    private void cargarTabla(List<Pelicula> lista) {
        // Define el modelo y las columnas
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"ID", "Titulo", "Director", "Anio", "Duracion", "Genero"});

        // Llenar el modelo con los datos de la lista
        for (Pelicula p : lista) {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getTitulo(),
                p.getDirector(),
                p.getAnio(),
                p.getDuracion(),
                p.getGenero()
            });
        }
        jTableCartelera.setModel(modelo);
    }


