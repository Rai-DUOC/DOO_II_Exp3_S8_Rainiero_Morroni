    private void btnFiltrarRangoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarRangoActionPerformed
        try {
            int inicio = Integer.parseInt(txtAnioInicio.getText());
            int fin = Integer.parseInt(txtAnioFin.getText());

            if (inicio > fin) {
                JOptionPane.showMessageDialog(this, "El anio de inicio debe ser menor o igual al anio de fin.");
                return;
            }

            List<Pelicula> listaFiltrada = dao.listarPorRangoAnios(inicio, fin);
            cargarTabla(listaFiltrada);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese numeros validos para el rango de anios.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnFiltrarRangoActionPerformed

    private void btnFiltrarGeneroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarGeneroActionPerformed
        String generoSeleccionado = (String) cmbGeneroFiltro.getSelectedItem();
        if (generoSeleccionado != null && !generoSeleccionado.isEmpty()) {
            List<Pelicula> listaFiltrada = dao.listarPorGenero(generoSeleccionado);
            cargarTabla(listaFiltrada);
        }
    }//GEN-LAST:event_btnFiltrarGeneroActionPerformed

